 class VariableExample{
    public static void main(String[] args){
        int i;
        i = 5;
        System.out.println(i);
        i = 10;
        System.out.println(i);
    }
}
class TypeExample{
    public static void main(String[] args){
        double d;
        d = 9.9;
        char c;
        c = 'a';
        byte b;
        b = 81;
        boolean bool;
        bool = true;
        System.out.println("double is " + d);
        System.out.println("char is " + c);
        System.out.println("byte is " + b);
        System.out.println("boolean is " + bool);
    }
}
 