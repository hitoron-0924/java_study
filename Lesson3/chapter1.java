class IfExample{
    public static void main(String[] args){
        int age;
        age = 18;
        if(age < 20){
            System.out.println("under 20");
        }
    }
}
class  ElseExample{
    public static void main(String[] args){
        int age;
        age = 20;
        if(age < 20){
            System.out.println("under 20");
        }
        else{
            System.out.println("over 20");
        }
    }
}